#include "../../include/minishell.h"

int	ft_pwd(t_shell *s)
{
	char	*pwd;

	pwd = getenv("PWD");
	if (!pwd)
	{
		pwd = s->pwd;
		if (!pwd)
			return (-1);
	}
	printf("%s\n", pwd);
	return (0);
}